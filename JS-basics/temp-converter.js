// Example: Convert 32° F to C using the Fahrenheit to Celsius formula.

let _fahrenheit = 32;

// by using the following scientific fomula  :  °C = (°F - 32) × 5/9;
 // get the expression below

let _selicius = (_fahrenheit - 32) * 5/9;


// (kelvin) by using the following scientific fomula  :  °k = (°F - 459.67) × 5/9;

let _kelvin = (_fahrenheit + 459.67) * 5 / 9;

console.log(`${_fahrenheit} egal to ${_selicius} °C`);

console.log(`${_fahrenheit} egal to ${_kelvin} °k`);